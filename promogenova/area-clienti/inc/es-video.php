<h5 class="viola">Incorporare il Codice di un video</h5>
<img src="<?php print $url; ?>/img/es-video.jpg" alt="es-video" class="scala" /><br />
1. Aprire una nuova finestra con il video che si desidera selezionare (nell'esempio: <a href="https://www.youtube.com/watch?v=yWDp1mVjN34">Dalle terre alle fornaci. Esposizione ceramiche di Angelo Baghino</a>)<br />
2. Puntare il <em>mouse</em> sotto il video e sotto il <strong>titolo</strong><br />
3. Nella men&ugrave; <em>Aggiungi / Condividi / Altro</em>, cliccare su <strong>Condividi</strong><br />
4. Nel sottomen&ugrave;, cliccare su <strong>Codice da incorporare</strong><br /> 
5. Copiare il codice<br />
6. Incollare il codice nel campo <strong>Codice del video</strong> su questa pagina<br />
