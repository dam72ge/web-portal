<h5 class="viola">Come si prende l'indirizzo URL (di qualsiasi pagina)</h5>
<img src="<?php print $url; ?>/img/es-url.jpg" alt="es-url" class="scala" /><br />
1. Aprire una nuova finestra con il sito o la pagina che si desidera copiare<br />
2. Puntare e poi cliccare il <em>mouse</em> sulla <strong>barra degli indirizzi</strong> in alto a sinistra<br />
3. Tenere premuto per selezionare (la selezione apparir&agrave; blu come sull'immagine qui sopra)<br />
4. Premere ALT+C (con questo comando si effettua una copia al volo)<br /> 
5. Tornare sulla finestra dell'Area clienti<br />
6. Cliccare nel campo <strong>Url</strong><br />
7. Premere ALT+V<br /><br />
